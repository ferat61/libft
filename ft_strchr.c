#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (s && *s != '\0')
	{
		if (*s == (char)c)
			return ((char *)s);
	++s;
	}
	if (s && c == '\0')
		return ((char *)s);
	return (0);
}
